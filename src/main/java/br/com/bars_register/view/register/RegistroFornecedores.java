    private void BtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnConfirmarActionPerformed
        String nome = txtNome.getText();
        String contato = txtContato.getText().replace(",", ".");
        String cnpj = txtCnpj.getText();
        String endereco = txtEndereco.getText();

        if (nome.isEmpty() | contato.isEmpty() | cnpj.isEmpty() | endereco.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
        } else {
            try {
                FornecedorDAO dao = new FornecedorDAO();

                Fornecedor fornecedor = new Fornecedor();

                fornecedor.setNome(nome);
                fornecedor.setContato(contato);
                fornecedor.setEndereco(endereco);
                fornecedor.setCnpj(cnpj);

                dao.salvarFornecedor(fornecedor);

                JOptionPane.showMessageDialog(this, "Fornecedor registrado!");
                fornecedoresFrame.atualizarTabelaFornecedores();
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_BtnConfirmarActionPerformed

